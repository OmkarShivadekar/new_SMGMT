package com.servletStore.report.generalRegister.model;

public class GeneralRegisterPOJO {
	int id;
	String section_name,trustyName,schoolName,address;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSection_name() {
		return section_name;
	}
	public void setSection_name(String section_name) {
		this.section_name = section_name;
	}
	
	public String getTrustyName() {
		return trustyName;
	}
	public void setTrustyName(String trustyName) {
		this.trustyName = trustyName;
	}
	public String getSchoolName() {
		return schoolName;
	}
	public void setSchoolName(String schoolName) {
		this.schoolName = schoolName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	
	
	

}
